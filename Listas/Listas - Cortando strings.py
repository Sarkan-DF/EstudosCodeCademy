animais = 'gatocobrapassaro'#lista simples

gato = animais[:4]#fatiando parte da string;
print(gato)#imprimindo parte da string;
cobra = animais[4:9]
print(cobra)
passaro = animais[9:]
print(passaro)
'''No Python, quando especificamos uma parte de uma lista dessa maneira,
incluímos o elemento com o primeiro da índice 0, mas excluímos o elemento
com o segundo índice 2.'''